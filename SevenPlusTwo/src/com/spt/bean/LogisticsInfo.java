package com.spt.bean;

public class LogisticsInfo {

	private String time;// 物流时间
	private String msg;// 物流信息

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

}
