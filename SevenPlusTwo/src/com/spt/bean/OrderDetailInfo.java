package com.spt.bean;

public class OrderDetailInfo {

	private Integer sendedCount;
	private Integer sendingCount;
	private Integer returnedCount;
	private String productName;
	private String styleNo;
	private String productColor;
	private String productSize;
	private String productBrand;
	private Double productPrice;
	private Integer productCount;

	public Integer getSendedCount() {
		return sendedCount;
	}

	public void setSendedCount(Integer sendedCount) {
		this.sendedCount = sendedCount;
	}

	public Integer getSendingCount() {
		return sendingCount;
	}

	public void setSendingCount(Integer sendingCount) {
		this.sendingCount = sendingCount;
	}

	public Integer getReturnedCount() {
		return returnedCount;
	}

	public void setReturnedCount(Integer returnedCount) {
		this.returnedCount = returnedCount;
	}

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getStyleNo() {
		return styleNo;
	}

	public void setStyleNo(String styleNo) {
		this.styleNo = styleNo;
	}

	public String getProductColor() {
		return productColor;
	}

	public void setProductColor(String productColor) {
		this.productColor = productColor;
	}

	public String getProductSize() {
		return productSize;
	}

	public void setProductSize(String productSize) {
		this.productSize = productSize;
	}

	public String getProductBrand() {
		return productBrand;
	}

	public void setProductBrand(String productBrand) {
		this.productBrand = productBrand;
	}

	public Double getProductPrice() {
		return productPrice;
	}

	public void setProductPrice(Double productPrice) {
		this.productPrice = productPrice;
	}

	public Integer getProductCount() {
		return productCount;
	}

	public void setProductCount(Integer productCount) {
		this.productCount = productCount;
	}

}
