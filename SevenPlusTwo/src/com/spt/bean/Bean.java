package com.spt.bean;


/**
 * 适配器数据
 */
public class Bean {
	private String name;//
	private String states;//状态 3种  0 选中  1 未选中 2不可选
	

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getStates() {
		return states;
	}

	public void setStates(String states) {
		this.states = states;
	}

}
